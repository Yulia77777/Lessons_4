package Task_1;

public class Human {
    private int weight;
    private int age;
    private String firstName;
    private String lastName;
    private int height;

    public Human(int weight, int age, String firstName, String lastName, int height) {
        this.weight = weight;
        this.age = age;
        this.firstName = firstName;
        this.lastName = lastName;
        this.height = height;
    }

}
